    def __init__(self, section: dict[str, any]) -> None:
        self._section = section

    def load(self) -> Event:
        return Event(
            default_winner_avatar=assets.image(
                self._section.get("event", {}).get(
                    "default_winner_avatar", "anon-ava.jpg"
                )
            ),
            winner_pictures=[
                Picture(
                    background=assets.image(
                        sub.get("background", "wanted-template.png")
                    ),
                    avatar_size=sub.get("avatar_size", 380),
                    avatar_position_x=sub.get("avatar_position_x", 150),
                    avatar_position_y=sub.get("avatar_position_y", 320),
                )
                for sub in self._section.get("event", {}).get("winner_pictures", [{}])
            ],
            play_categories=[
                self._espers_category,
                self._chakra_category,
                self._kawaii_category,
                self._alchemy_category,
                self._titan_power_category,
                self._cooking_category,
                self._detective_category,
                self._shinigami_category,
                self._breathing_category,
                self._luck_category,
            ],
        )
